for (let i = 0; i < alpha.length; i++) {
    var b = alpha[i][0].toUpperCase() + alpha[i].slice(1);

    alphas.push(b)
}

button.forEach((button) => {

    button.addEventListener("click", function () {

        let btn = button.value.toUpperCase();
        for (let alphabets of alphas) {
            if (alphabets[0] === btn) {
                words.push(alphabets)
            }
        }
        if (words.length > 0) {
            let index = Math.floor(Math.random() * words.length)
            show.innerHTML = words[index];
            let speak = window.speechSynthesis;
            let sound =  speak.getVoices();
            let voice = new SpeechSynthesisUtterance(`${words[index]}`);
            voice.voice = sound[2];
            voice.pitch = 1.8;
            voice.rate = 2; 
            speak.speak(voice)
            words = [];
        }
    })
});





document.addEventListener("keydown", function (e) {
    let key = e.key;
    if ((key >= "A" && key <= "Z") || (key >= "a" && key <= "z")) {


        for (let alphabets of alphas) {
            if (alphabets[0].toLowerCase() == key.toLowerCase()) {
                words.push(alphabets)
            }
        }
        if (words.length > 0) {
            let index = Math.floor(Math.random() * words.length)
            show.innerHTML = words[index];
            let speak = window.speechSynthesis;
            let sound = speak.getVoices();
            let voice = new SpeechSynthesisUtterance(`${words[index]}`);
            voice.voice = sound[2];
            voice.pitch =1.8;
            voice.rate = 2 ;
            speak.speak(voice)
            words = [];
        }
    }

})




